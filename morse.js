function tx(length,time) {
  setTimeout(function () {
    var context = new (window.AudioContext)()
    var osc = context.createOscillator()
    osc.type = 'sine'
    osc.frequency.value = 500
    osc.connect(context.destination)
    osc.start()
    osc.stop(context.currentTime + length)
  }, time)
}
